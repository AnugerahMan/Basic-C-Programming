#include <stdio.h>

int main(){
	int X = 4;
	int Y = 7;
	
	/*Output operator pembanding berupa boolean*/
	printf("Hasil dari X>Y = %d\n", X>Y);
	printf("Hasil dari X<Y = %d\n", X<Y);
	printf("Hasil dari X<=Y = %d\n", X<=Y);
	printf("Hasil dari X>=Y = %d\n", X>=Y);
	printf("Hasil dari X==Y = %d\n", X==Y);
	printf("Hasil dari X!=Y = %d\n", X!=Y);
	printf("Hasil dari X!=Y = %d\n", X!=X);
	return 0;
}
